-- init_db.sql

CREATE TABLE AUTORY (
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY
	PRIJMENI VARCHAR(50) NOT NULL,
	JMENO VARCHAR(50)
	);
	
CREATE TABLE NAKLADATELSTVI (
	ID INTEGER NOT NULL PRIMARY KEY,
	NAZEV_FIRMY VARCHAR(100),
	MESTO VARCHAR(50)
	);
	
CREATE TABLE KNIHY (
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	NAZEV VARCHAR(100) NOT NULL,
	ROK INTEGER,
	AUTOR_ID INTEGER NOT NULL,
	NAKLADATELSTVI_ID INTEGER,
	POCET_STRAN INTEGER,
	FOREIGN KEY (AUTOR_ID) REFERENCES AUTORY(ID),
	FOREIGN KEY (NAKLADATELSTVI_ID) REFERENCES NAKLADATELSTVI(ID)
	);

CREATE TABLE KNIHY_DETAILY (
	KNIHYDET_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	POPIS_KNIHY VARCHAR(400),
	KNIHY_CESTA VARCHAR(255),
	FOREIGN KEY (KNIHYDET_ID) REFERENCES KNIHY(ID)
	);

CREATE TABLE ZANRY (
	ZANRY_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	NAZEV_ZANRY VARCHAR(100) UNIQUE
	);

CREATE TABLE KNIHY_ZANRY (
	KNIHY_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	ZANRY_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	PRIMARY KEY(KNIHY_ID, ZANRY_ID),
	FOREIGN KEY (KNIHY_ID) REFERENCES KNIHY(ID),
	FOREIGN KEY (ZANRY_ID) REFERENCES ZANRY(ZANRY_ID)
    );
	
	
